import { writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { resolve, relative } from 'node:path'
import type { ResolvedFluentConfig } from './types'

/**
 * Generate the server module that provides:
 * - setLocale / getI18n / __getServerI18n (for t`` in RSC)
 * - Trans / Plural / DateTime / NumberFormat (server components)
 * - FluentProvider (async server component for layouts)
 *
 * @returns Absolute path to the generated server module.
 */
export function generateServerModule(
  projectRoot: string,
  config: ResolvedFluentConfig,
): string {
  if (config.serverModule) {
    return resolve(projectRoot, config.serverModule)
  }

  const outDir = resolve(projectRoot, config.serverModuleOutDir)
  const outPath = resolve(outDir, 'server.js')
  const dtsPath = resolve(outDir, 'server.d.ts')

  if (!existsSync(outDir)) {
    mkdirSync(outDir, { recursive: true })
  }

  const compiledDirAbs = resolve(projectRoot, config.compiledDir)
  const compiledRelative = toForwardSlash(relative(outDir, compiledDirAbs))

  const localeImports = config.locales
    .map((locale) => `      case '${locale}': return import('${compiledRelative}/${locale}')`)
    .join('\n')

  const fallbackChainStr = config.fallbackChain
    ? JSON.stringify(config.fallbackChain)
    : 'undefined'

  // Generate a 'use client' provider that imports messages statically.
  // Messages contain functions (interpolation) which can't cross the RSC boundary.
  const clientProviderPath = resolve(outDir, 'client-provider.js')

  const clientStaticImports = config.locales
    .map((locale) => {
      const safe = locale.replace(/[^a-zA-Z0-9]/g, '_')
      return `import ${safe} from '${compiledRelative}/${locale}'`
    })
    .join('\n')

  const clientAllMessagesEntries = config.locales
    .map((locale) => {
      const safe = locale.replace(/[^a-zA-Z0-9]/g, '_')
      return `'${locale}': ${safe}`
    })
    .join(', ')

  const clientProviderSource = `"use client";
// Auto-generated by @fluenti/next — do not edit
import { createElement } from 'react'
import { I18nProvider } from '@fluenti/react'
${clientStaticImports}

const __allMessages = { ${clientAllMessagesEntries} }

export function ClientI18nProvider({ locale, fallbackLocale, fallbackChain, children }) {
  return createElement(I18nProvider, { locale, fallbackLocale, messages: __allMessages, fallbackChain }, children)
}
`
  writeFileSync(clientProviderPath, clientProviderSource, 'utf-8')

  const moduleSource = `// Auto-generated by @fluenti/next — do not edit
import { createServerI18n } from '@fluenti/react/server'
import { createElement } from 'react'

const serverI18n = createServerI18n({
  loadMessages: async (locale) => {
    switch (locale) {
${localeImports}
      default: return import('${compiledRelative}/${config.defaultLocale}')
    }
  },
  fallbackLocale: '${config.defaultLocale}',
  fallbackChain: ${fallbackChainStr},
  resolveLocale: async () => {
    try {
      const { cookies } = await import('next/headers')
      return (await cookies()).get('locale')?.value ?? '${config.defaultLocale}'
    } catch {
      return '${config.defaultLocale}'
    }
  },
})

export const setLocale = serverI18n.setLocale
export const getI18n = serverI18n.getI18n
export const Trans = serverI18n.Trans
export const Plural = serverI18n.Plural
export const DateTime = serverI18n.DateTime
export const NumberFormat = serverI18n.NumberFormat

/**
 * Synchronous accessor used by the webpack loader for t\`\` in RSC.
 */
export const __getServerI18n = serverI18n.__getSyncInstance

/**
 * Async server component for root layouts.
 *
 * Sets up both server-side (React.cache) and client-side (I18nProvider) i18n.
 */
export async function FluentProvider({ locale, children }) {
  const activeLocale = locale ?? '${config.defaultLocale}'

  // 1. Initialize server-side i18n (React.cache scoped)
  serverI18n.setLocale(activeLocale)
  await serverI18n.getI18n()

  // 2. Import the local 'use client' provider that has messages statically bundled.
  // Messages contain functions (interpolation) which can't be serialized across the RSC boundary.
  const { ClientI18nProvider } = await import('./client-provider.js')

  return createElement(ClientI18nProvider, {
    locale: activeLocale,
    fallbackLocale: '${config.defaultLocale}',
    fallbackChain: ${fallbackChainStr},
  }, children)
}
`

  const dtsSource = `// Auto-generated by @fluenti/next — do not edit
import type { ReactNode, ReactElement } from 'react'
import type { FluentInstanceExtended } from '@fluenti/core'

export declare function setLocale(locale: string): void
export declare function getI18n(): Promise<FluentInstanceExtended & { locale: string }>
export declare function __getServerI18n(): FluentInstanceExtended & { locale: string }

export declare function Trans(props: {
  children: ReactNode
  id?: string
  comment?: string
  render?: (translation: ReactNode) => ReactNode
}): Promise<ReactElement>

export declare function Plural(props: {
  value: number
  zero?: ReactNode
  one?: ReactNode
  two?: ReactNode
  few?: ReactNode
  many?: ReactNode
  other: ReactNode
  offset?: number
}): Promise<ReactElement>

export declare function DateTime(props: {
  value: Date | number
  style?: string
}): Promise<ReactElement>

export declare function NumberFormat(props: {
  value: number
  style?: string
}): Promise<ReactElement>

export declare function FluentProvider(props: {
  locale?: string
  children: ReactNode
}): Promise<ReactElement>
`

  writeFileSync(outPath, moduleSource, 'utf-8')
  writeFileSync(dtsPath, dtsSource, 'utf-8')

  return outPath
}

function toForwardSlash(p: string): string {
  return p.split('\\').join('/')
}
